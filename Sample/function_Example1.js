const calculator = {
    add: function(a, b) {
        console.log(a + b);
    },

    minus: function(a, b) {
        console.log(a - b);
    },

    multiple: function(a, b) {
        console.log(a * b);
    },

    division: function(a, b) {
        console.log(a / b);
    },

    pow: function(a, b) {
        console.log(a ** b);
    },
};

calculator.add(7, 2);
calculator.minus(7, 2);
calculator.multiple(7, 2);
calculator.division(7, 2);
calculator.pow(7, 2);


